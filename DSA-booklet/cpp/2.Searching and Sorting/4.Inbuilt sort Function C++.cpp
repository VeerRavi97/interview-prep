bool comp(int a, int b)
{
    return a < b;
}
int main()
{
    clock_t start = clock();
    file_io();
    int tc;
    cin >> tc;
    while (tc--)
    {
        int n;
        cin >> n;
        vi v(n, 0);
        for (int i = 0; i < n; i++)
            cin >> v[i];
        sort(all(v));
        for (auto x : v)
            cout << x << " ";
        cout << endl;
        sort(all(v), greater<int>());
        for (auto x : v)
            cout << x << " ";
        cout << endl;

        sort(all(v), comp);
        for (auto x : v)
            cout << x << " ";
        cout << endl;
    }

    clock_t end = clock();
#ifndef ONLINE_JUDGE
    double cpu_time_used = ((double)(end - start)) / CLOCKS_PER_SEC;
    cout << "\n\nExecuted In: " << cpu_time_used << "s" << endl;
#endif
}